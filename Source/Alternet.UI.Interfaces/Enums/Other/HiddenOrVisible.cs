using System;
using System.Collections.Generic;
using System.Text;

namespace Alternet.UI
{
    /// <summary>
    /// Enumerates visibility options of the visual element.
    /// </summary>
    public enum HiddenOrVisible
    {
        /// <summary>
        /// Visibility is determined automatically.
        /// </summary>
        Auto,

        /// <summary>
        /// Element is always hidden.
        /// </summary>
        Hidden,

        /// <summary>
        /// Element is always visible.
        /// </summary>
        Visible,
    }
}
