namespace Alternet.Drawing
{
    /// <summary>
    /// Specifies a generic font family.
    /// </summary>
    public enum GenericFontFamily
    {
        /// <summary>
        /// Not specified.
        /// </summary>
        None = 0,

        /// <summary>
        /// A generic 'Sans Serif' (Swiss) font family.
        /// </summary>
        SansSerif = 1,

        /// <summary>
        /// A generic 'Serif' (Roman) font family.
        /// </summary>
        Serif = 2,

        /// <summary>
        /// A generic 'Monospace' (Teletype, fixed pitch font) font family.
        /// </summary>
        Monospace = 3,

        /// <summary>
        /// Default font family.
        /// </summary>
        Default = 4,
    }
}