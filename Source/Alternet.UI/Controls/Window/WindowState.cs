namespace Alternet.UI
{
    /// <summary>
    /// Specifies how a window is displayed.
    /// </summary>
    public enum WindowState
    {
        /// <summary>
        /// A default sized window.
        /// </summary>
        Normal,

        /// <summary>
        /// A minimized window.
        /// </summary>
        Minimized,

        /// <summary>
        /// A maximized window.
        /// </summary>
        Maximized
    }
}